package com.anp.Library;

import javax.persistence.*;

//The @Entity annotation indicates that this class is an entity that will be mapped to a database table.
@Entity
public class Member {

    @Id     //The @Id annotation specifies the primary key field of the entity.

    @GeneratedValue(strategy = GenerationType.IDENTITY)   //indicates that the primary key values are automatically generated by the database.

    //attributes of the Member entity.
    private int id;

    private String name;
    private int age;
    private String address;
    private String membershipType;
    private boolean active;

    // Default constructor
    public Member() {
    }

    // Constructor with parameters
    public Member(int id,String name, int age, String address, String membershipType, boolean active) {
    	this.id = id;
        this.name = name;
        this.age = age;
        this.address = address;
        this.membershipType = membershipType;
        this.active = active;
    }

    
    // Getter and setter methods

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getMembershipType() {
        return membershipType;
    }

    public void setMembershipType(String membershipType) {
        this.membershipType = membershipType;
    }

    public boolean isActive() {
        return active;
    }

    public void setActive(boolean active) {
        this.active = active;
    }
//The toString method is overridden to provide a string representation of the Member object, including its attributes.

	@Override
	public String toString() {
		return "Member [id=" + id + ", name=" + name + ", age=" + age + ", address=" + address + ", membershipType="
				+ membershipType + ", active=" + active + "]";
	}
}

